#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	num;

	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	num = (nb % 10) + '0';
	write(1, &num, 1);
}

